from .mapping import FAIL, PASS, MANUAL, NOT_ASSESSED, summarise

ICON = {FAIL: "❌", PASS: "✅", MANUAL: "📋", NOT_ASSESSED: "⚠️"}
LABEL = {FAIL: "Not compliant", PASS: "Compliant", MANUAL: "Manual", NOT_ASSESSED: "Not assessed"}


# ---------------------------------------------------------------- Markdown ----
def build_markdown(framework, control_results, ctx, narrate):
    s = summarise(control_results)
    L = []
    L.append(f"# {framework.name} — AWS Compliance Report")
    L.append("")
    L.append(f"**Account:** {ctx.get('account') or 'n/a'}  ·  "
             f"**Region:** {ctx.get('region') or 'n/a'}  ·  "
             f"**Assessed:** {ctx.get('date') or 'n/a'}  ·  "
             f"**Engine:** {ctx.get('engine', 'template')}")
    L.append("")
    L.append("## Executive summary")
    L.append("")
    L.append(f"This report assesses the account against **{framework.name}**"
             f"{(' v' + framework.version) if framework.version else ''}. "
             f"Of **{s['automated']} automated controls**, **{s['pass']} passed** and "
             f"**{s['fail']} failed** — an automated compliance score of **{s['score']}%**. "
             f"A further **{s['manual']} controls require manual assessment** "
             f"(governance/process obligations not observable from AWS APIs)"
             + (f", and **{s['not_assessed']} were not assessed** (no matching findings supplied)." if s['not_assessed'] else "."))
    L.append("")
    if s["fails"]:
        L.append("**Priority controls not met:**")
        for r in s["fails"]:
            L.append(f"- ¶{r.control.paragraph} — {r.control.name} ({len(r.fails)} finding(s))")
        L.append("")

    # group by section, preserving order
    sections = OrderedDict()
    for r in control_results.values():
        sections.setdefault(r.control.section or "Other", []).append(r)

    L.append("## Control-by-control assessment")
    for section, rows in sections.items():
        L.append("")
        L.append(f"### {section}")
        for r in rows:
            c = r.control
            L.append("")
            L.append(f"#### {ICON.get(r.status,'•')} ¶{c.paragraph} — {c.name}  · {LABEL.get(r.status, r.status)}")
            L.append("")
            L.append(narrate(r))
            if r.fails:
                L.append("")
                L.append("> **Evidence:**")
                for f in r.fails[:8]:
                    L.append(f"> - {f['detail']}" + (f"  `{f['resource']}`" if f['resource'] else ""))
                if len(r.fails) > 8:
                    L.append(f"> - …and {len(r.fails) - 8} more")
            if c.reference:
                L.append("")
                L.append(f"_Reference: {c.reference}_")

    if s["fails"]:
        L.append("")
        L.append("## Remediation roadmap")
        for i, r in enumerate(s["fails"], 1):
            L.append(f"{i}. **¶{r.control.paragraph} {r.control.name}** — {r.control.remediation}")

    L.append("")
    L.append("---")
    L.append("_Generated by AiOpsOne Assure. Automated controls reflect Prowler / AWS Config "
             "findings; manual controls require human assessment. Verify against the official "
             "APRA standard. Not legal advice._")
    return "\n".join(L)


