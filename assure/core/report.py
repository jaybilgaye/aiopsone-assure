_BOLD = re.compile(r"\*\*(.+?)\*\*")


def _inline(text):
    """Escape HTML then apply minimal markdown inline (**bold** only).

    We intentionally do NOT convert single-* to italic: framework text contains
    literal IAM wildcards like ``*:*`` / ``s3:*`` that must render verbatim.
    """
    out = html.escape(text or "")
    out = _BOLD.sub(r"<strong>\1</strong>", out)
    return out


